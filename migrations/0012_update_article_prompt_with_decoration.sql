UPDATE prompts 
SET body = 'あなたはプロのWebライターです。以下の指示に従って、SEOに最適化された高品質な記事を執筆してください。

【キーワード】
{{keyword}}

【記事構成】
{{outline}}

【最重要：文字数制約】
⚠️ 必ず {{max_chars}} 文字程度で執筆してください。これは絶対条件です。
- 目標文字数: {{max_chars}} 文字
- 許容範囲: {{max_chars}}文字の±10%以内

【執筆要件】
- 文体: {{tone}}な表現で執筆
- フォーマット: Markdown形式で出力
- 見出し: ##（H2）、###（H3）を適切に使用

【必須：装飾ルール】
以下の装飾を必ず使用して、分かりやすい記事を作成してください：

1. 箇条書き（必須）
   - 3つ以上のポイントを列挙する場合は必ず箇条書きを使用
   - 例：特徴、メリット、手順、注意点などは箇条書きで

2. 太字で強調（必須）
   - 重要なキーワードや結論は太字で強調
   - 1段落に1-2箇所使用

3. ボックス（引用）で補足（必須）
   - 各セクションに1-2個のボックスを配置
   - 形式例：
     > 💡 ポイント: 重要な補足情報
     > ⚠️ 注意: 注意すべき内容
     > ✅ メリット: メリットや利点

4. 表（テーブル）の活用
   - 比較や選択肢が3つ以上ある場合は表を使用
   - Markdown表形式で記述

【記事の構造】
1. 導入（リード文）: 読者の興味を引く（全体の10%）
2. 本文: 構成に従って詳しく解説（全体の80%）
   - 各セクションで箇条書き、太字、ボックスを活用
   - 適切に表を挿入
3. まとめ: 重要なポイントを箇条書きで再確認（全体の10%）

【注意事項】
- プロンプトや指示文をそのまま出力しない
- すぐに本文から始める
- 装飾を適度に使用（過度な装飾は避ける）
- 自然で読みやすい日本語で執筆

それでは、{{max_chars}}文字で、箇条書き・太字・ボックス・表を効果的に使った記事を執筆してください。'
WHERE type = 'article_draft';
